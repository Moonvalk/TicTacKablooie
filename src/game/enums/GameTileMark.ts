/**
 * Provides an index for each possible mark a game tile may have.
 */
 export enum GameTileMark {
    /**
     * This tile displays no marking.
     */
    None,

    /**
     * This tile displays the "X" (player 1) marking.
     */
    X,

    /**
     * This tile displays the "O" (player 2) marking.
     */
    O
}